package loopstudy;

public class ForLoopStudy {

	public static void main(String[] args)
	{
       // i want to print teble of 2
	   //start with ----2 finish with-----20 update by----i+2
		for(int i=2; i<=20; i=i+2)
		{
			System.out.println(i);
			
		}
		System.out.println("********************************************");
		//i want to print tebal of 2 in revars order
		//start with 20------finish with 2------------update by-------i-2
		for(int i=20; i>=2; i=i-2)
		{
			System.out.println(i);
		}
	}

}
